const stemToElement = {
    갑: "목", 을: "목",
    병: "화", 정: "화",
    무: "토", 기: "토",
    경: "금", 신: "금",
    임: "수", 계: "수",
};
/**
 * 일간에 따라 십성 색상 팔레트 결정
 */
export function getTenGodColors(dayStem) {
    const el = stemToElement[dayStem] ?? "목";
    return paletteByElement[el];
}
